package com.il.appshortcut.views;

import android.graphics.drawable.Drawable;

public class ActivityDetailVo {
	private int idActivityOption;
	private int type;
	private int order;
	private int top;
	private int actionType;
	private int idActivity;
	private int idAction;
	private Drawable icon;
	
	public int getIdActivityOption() {
		return idActivityOption;
	}
	public void setIdActivityOption(int idActivityOption) {
		this.idActivityOption = idActivityOption;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public int getOrder() {
		return order;
	}
	public void setOrder(int order) {
		this.order = order;
	}
	public int getTop() {
		return top;
	}
	public void setTop(int top) {
		this.top = top;
	}
	public int getActionType() {
		return actionType;
	}
	public void setActionType(int actionType) {
		this.actionType = actionType;
	}
	public int getIdActivity() {
		return idActivity;
	}
	public void setIdActivity(int idActivity) {
		this.idActivity = idActivity;
	}
	public int getIdAction() {
		return idAction;
	}
	public void setIdAction(int idAction) {
		this.idAction = idAction;
	}
	public Drawable getIcon() {
		return icon;
	}
	public void setIcon(Drawable icon) {
		this.icon = icon;
	}
	
}
